export const generateAgroecologyProfile = async (
  homesteadName: string,
  farmName: string,
  lat: number,
  lng: number,
  corners?: Array<{ lat: number; lng: number }>
): Promise<string> => {
  try {
    const isKenya = lat >= -4.72 && lat <= 4.62 && lng >= 33.9 && lng <= 41.9;

    let soilMoistureData = 'Failed to retrieve openEO soil moisture.';
    try {
       const moistureRes = await getOpenEO_SoilMoisture(lat, lng);
       if (moistureRes) {
          soilMoistureData = JSON.stringify(moistureRes);
       }
    } catch(e) { console.error(e) }

    let soilStaticData = '';

    if (isKenya) {
      try {
        const rcmrd = await getRCMRDSoilData(lat, lng);
        soilStaticData = `Provider: RCMRD. Data: ${JSON.stringify(rcmrd)}`;
      } catch(e){ console.error(e) }
    } else {
      try {
        const sg = await getSoilGridsFallback(lat, lng);
        soilStaticData = `Provider: SoilGrids. Data: ${JSON.stringify(sg)}`;
      } catch(e){ console.error(e) }
    }

    const dataStrategySource = isKenya 
      ? `For Soil Moisture (Dynamic): openEO ecosystem accessing Copernicus Sentinel-1 Satellite Data.
For pH and Soil Type (Static): Local Providers (RCMRD) for high-accuracy regional data.`
      : `For Soil Moisture (Dynamic): openEO ecosystem accessing Copernicus Sentinel-1 Satellite Data.
For pH and Soil Type (Static): SoilGrids (Global Baseline). Note: Recommend the integration of local providers for more accurate data in this region.`;

    let boundaryContext = "";
    if (corners && corners.length === 4) {
      const areaData = calculateAreaFromCorners(corners);
      boundaryContext = `
The plot is surveyed with a custom 4-corner boundary (quadrilateral):
- Corner A: (${corners[0].lat.toFixed(6)}, ${corners[0].lng.toFixed(6)})
- Corner B: (${corners[1].lat.toFixed(6)}, ${corners[1].lng.toFixed(6)})
- Corner C: (${corners[2].lat.toFixed(6)}, ${corners[2].lng.toFixed(6)})
- Corner D: (${corners[3].lat.toFixed(6)}, ${corners[3].lng.toFixed(6)})
${areaData ? `- Calculated Area: ${areaData.areaSqm.toFixed(1)} m² (${areaData.hectares.toFixed(3)} Hectares, ${areaData.acres.toFixed(3)} Acres)` : ""}
`;
    }

    const prompt = `You are the Agroecology AI Engine.
Generate a detailed Agroecology Profile for a farm plot located at coordinates (${lat.toFixed(4)}, ${lng.toFixed(4)}).
The homestead is "${homesteadName}" and the plot is "${farmName}".
${boundaryContext}

Data Sourcing Strategy for this Request:
${dataStrategySource}

Current API Data Context:
- Soil Moisture: ${soilMoistureData}
- Soil Physical Static Data: ${soilStaticData}

Provide:
1. **Soil Information Strategy & Estimates**: Explicitly state the data sourcing strategy used. Provide the expected soil type, pH, and live moisture estimates based on the API Data Context provided above.
2. **Detailed Agroecology Recommendations** (crops to plant, soil management, water harvesting). Recommend a wide window of food crops depending on the soil data, including indigenous food crops and crops common to the region in question. Give farmers a variety of options to select from. Also consider if the climate and soil can support: tomatoes, kales, cabbage, rice, specific types of beans, groundnuts, maize, yam, cassava, potatoes, bananas, watermelon, etc. Do not hallucinate, rely strictly on agricultural truth for the given coordinates.
3. **A Suggested Seasonal Calendar** (management of inputs and harvest).
Ensure it is formatted using clean Markdown. Focus on regenerative agriculture and climate resilience. Keep it concise but comprehensive.`;

    const response = await fetch('/api/gemini', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify({
           prompt,
           model: 'gemini-2.5-flash',
           systemInstruction: "You are an expert Agroecology AI.",
           temperature: 0.7
        })
    });

    if (!response.ok) {
         const errText = await response.text();
         let errMsg = 'Failed to generate profile';
         try {
             const errObj = JSON.parse(errText);
             errMsg = errObj.error || errObj.message || errText || errMsg;
         } catch {
             errMsg = errText || errMsg;
         }
         throw new Error(errMsg);
    }

    const resText = await response.text();
    if (!resText) {
        throw new Error("Empty response from AI server");
    }
    
    let data;
    try {
        data = JSON.parse(resText);
    } catch (e) {
        throw new Error("Failed to parse response: " + resText);
    }
    
    return data.text || "No profile generated.";
  } catch (error) {
    console.error("Error generating agroecology profile, using custom realistic fallback generator:", error);
    return getRealisticFallbackAgroecologyProfile(homesteadName, farmName, lat, lng, corners);
  }
};
